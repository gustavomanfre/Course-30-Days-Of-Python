# Operadores Matematicos
print(3+2)      # Sumatoria (+) -> Imprime 5
print(3-2)      # Resta (-) -> Imprime 1
print(3*2)      # Multiplication (*)-> Imprime 6
print(3/2)      # Division Punto Flotante -> Imprime 1.5
print(3**2)     # Exponential (**), imprime 9
print(3%2)      # Modulus (%), imprime -> 1
print (3//2)    # División Entera(//), imprime 1

